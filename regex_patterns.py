"""
Регулярные выражения для проверки IPv6 адресов
"""

import re

# Расширенное регулярное выражение для IPv6 адресов

IPV6_PATTERN = re.compile(r'''
    # Полная форма (8 групп по 4 hex цифры)
    (?:[0-9a-fA-F]{1,4}:){7}[0-9a-fA-F]{1,4}
    |
    # Сокращенная форма с ::
    (?:
        (?:[0-9a-fA-F]{1,4}:)*
        ::
        (?:[0-9a-fA-F]{1,4}:)*
    )?
    (?:[0-9a-fA-F]{1,4}:)*[0-9a-fA-F]{1,4}
    |
    # IPv4-совместимые адреса
    (?:[0-9a-fA-F]{1,4}:){6}(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)
    |
    # Формат с зоной (интерфейсом)
    [0-9a-fA-F:]+%[a-zA-Z0-9]+
''', re.VERBOSE | re.IGNORECASE)